public class TeacherProfile {
    public static void main(String[] args) {
        // 定义变量name表示姓名
        String name = "程序员美羊羊";
        // 定义变量age表示年龄
        int age = 21;
        // 定义变量gender表示性别
        char gender = '女';
        // 定义变量height表示身高
        double height = 167.8;
        // 定义变量along表示是否单身
        boolean along = true;
        
        // 输出老师信息
        System.out.println("姓名：" + name);
        System.out.println("年龄：" + age);
        System.out.println("性别：" + gender);
        System.out.println("身高：" + height + "cm");
        System.out.println("单身状态：" + along);
    }
}