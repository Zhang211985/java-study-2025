public class Value{
    public static void main(String[] args){
    	//打印整数、小数
    	System.out.println(13);    //正整数
    	System.out.println(-13);    //负整数
    	System.out.println(1.3);    //正小数
    	System.out.println(-1.3);    //负小数
    	//打印字符
    	System.out.println('A');    //字母
    	System.out.println('O');    //数字
    	System.out.println("我");    //汉字
    	//打印字符串
    	System.out.println("HelloWorld");
   	 System.out.println("我爱写代码");
   	 //打印布尔
    	System.out.println(true);
   	 System.out.println(false);
    	//打印空值
    	System.out.println("null");
    }
}